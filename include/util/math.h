#ifndef MATH_H
#define MATH_H

/**
 * Get the minimum out of two values
 * @param [in] a
 * Some number
 * @param [in] b
 * Some number
 * @return The minimum
 */
#define min(a, b) a < b ? a : b

/**
 * Get the maximum out of two values
 * @param [in] a
 * Some number
 * @param [in] b
 * Some number
 * @return The maximum
 */
#define max(a, b) a > b ? a : b

#endif// MATH_H
